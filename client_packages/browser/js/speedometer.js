var speedometer = new Vue({
    el: "#speedometer",
    data: {
        show: false,
        headlights: 0, //0-выкл,1-габариты,2-ближний,3-дальний (фары)
        lock: 0, //0-открыт,1-закрыт (двери)
        speed: 240,
        fuel: 50,
        maxFuel: 70,
        mileage: 23004,
        danger: 0, //0-выкл,1-вкл (аварийка)
        maxSpeed: 480,
        svgPaths: dataForSpeedScale,
    },
    computed: {
        compSpeed: function () {
            let speed = this.speed;

            let amountLines = this.svgPaths.length;
            let step = this.maxSpeed / (amountLines - 1);
            let count = Math.floor(speed / step);

            for (let i = 0; i < amountLines; i++) {
                let val = i <= count;

                if (speed == 0) val = false;

                this.svgPaths[i].isFill = val;
            }

            return speed;
        }
    }


});

